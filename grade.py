score = int(input("score: "))

if score >= 90 and score <= 100:
    print("Grade: A" + " " + "pass")

elif score >= 80 and score <=100:
    print("Grade: B" + " " + "pass")

elif score >= 70 and score <= 100:
    print("Grade: C " + " " + "average")

else: 
    print("Grade: F" + " " + "fail")
            
